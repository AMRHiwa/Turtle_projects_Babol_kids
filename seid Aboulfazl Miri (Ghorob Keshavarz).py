r3 = turtle.Turtle()




r3.pencolor("brown")

r3.penup()
r3.goto(500,-300)
r3.pendown()
r3.left(90)

def tree(i):
    if i<10:
        return
    else:
        r3.forward(i)
        r3.left(30)
        tree(3*i/4)
        r3.right(60)
        tree(3*i/4)
        r3.left(30)
        r3.backward(i)


tree(100)

